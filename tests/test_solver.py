import pytest

from sudoku_solver.board import SudokuBoard
from sudoku_solver.exceptions import InvalidBoardError
from sudoku_solver.solver import SudokuSolver


def test_solver_solves_a_valid_puzzle() -> None:
    """Solver should solve a valid sudoku puzzle."""
    puzzle = [
        [5, 3, 0, 0, 7, 0, 0, 0, 0],
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
        [0, 9, 8, 0, 0, 0, 0, 6, 0],
        [8, 0, 0, 0, 6, 0, 0, 0, 3],
        [4, 0, 0, 8, 0, 3, 0, 0, 1],
        [7, 0, 0, 0, 2, 0, 0, 0, 6],
        [0, 6, 0, 0, 0, 0, 2, 8, 0],
        [0, 0, 0, 4, 1, 9, 0, 0, 5],
        [0, 0, 0, 0, 8, 0, 0, 7, 9],
    ]

    board = SudokuBoard(puzzle)
    solver = SudokuSolver()

    solved = solver.solve(board)

    assert solved is True
    assert board.find_next_empty() is None


def test_invalid_puzzle_raises_error() -> None:
    """Invalid starting puzzles should raise an exception."""
    puzzle = [
        [5, 5, 0, 0, 7, 0, 0, 0, 0],
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
        [0, 9, 8, 0, 0, 0, 0, 6, 0],
        [8, 0, 0, 0, 6, 0, 0, 0, 3],
        [4, 0, 0, 8, 0, 3, 0, 0, 1],
        [7, 0, 0, 0, 2, 0, 0, 0, 6],
        [0, 6, 0, 0, 0, 0, 2, 8, 0],
        [0, 0, 0, 4, 1, 9, 0, 0, 5],
        [0, 0, 0, 0, 8, 0, 0, 7, 9],
    ]

    with pytest.raises(InvalidBoardError):
        SudokuBoard(puzzle)